async function generateResponse(question) {
  const q = question.toLowerCase();

  // 1ï¸âƒ£ Risposte locali da scripts.json
  for (const key in scripts) {
    if (q.includes(key.toLowerCase())) {
      const replies = scripts[key];
      return replies[Math.floor(Math.random() * replies.length)];
    }
  }

  // 2ï¸âƒ£ AI avanzata via Ollama
  try {
    const response = await fetch("http://localhost:11434/api/generate", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        model: "phi3:mini",
        prompt: `${aiInstructions}\nUtente: ${question}\nRisposta:`
      })
    });

    const data = await response.json();
    if (data?.response) return data.response.trim();
  } catch (err) {
    console.log("âš ï¸ AI locale non raggiungibile:", err.message);
  }

  // 3ï¸âƒ£ Fallback finale â†’ escalation staff
  return "Sto inoltrando questa richiesta allo staff. Attendere.";
}

